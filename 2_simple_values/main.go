package main

import "fmt"

func main() {
    //normal values
	//integer

	fmt.Println(1)
	fmt.Println(1+1)
	fmt.Println(2-1)

	//float
	fmt.Println(1.5)
	fmt.Println(1.5+1.5)
	fmt.Println(2.5-1.5)
	fmt.Println(2.5*2)
	fmt.Println(2.5/2)

	//string
	fmt.Println("hello")

	//boolean
	fmt.Println(true)
	fmt.Println(false) 

}