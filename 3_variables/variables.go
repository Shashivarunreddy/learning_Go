package main

import "fmt"

func main() {
	
	//integer
	var num int = 1
	var num2 = 2
	num3 := 3

	fmt.Println(num)
	fmt.Println(num2)	
	fmt.Println(num3)

	//float
	var numf float32 = 1.5
	var numf2 = 2.5
	numf3 := 3.5
	
	fmt.Println(numf)
	fmt.Println(numf2)
	fmt.Println(numf3)

	//sting
	var str string = "hello"
	var str2 = "hello world"
	str3 := "hello world !!!"
	
	fmt.Println(str)
	fmt.Println(str2)
	fmt.Println(str3)

	//boolean
	var b1 bool = true
	var b2 = false
	b3 := true

				
	fmt.Println(b1)
	fmt.Println(b2)
	fmt.Println(b3)
			

}